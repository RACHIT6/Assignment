// C Program to Add Two Integers.
#include<stdio.h>
int main(int argc, char const *argv[])
{
    int a, b;
    printf("Enter The First integer to add\n");
    scanf("%d", &a);
    printf("Enter The Second integer to add\n");
    scanf("%d", &b);
    printf("The sum is %d\n", a + b);

    return 0;
}